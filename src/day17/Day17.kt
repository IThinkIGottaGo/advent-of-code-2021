 *
 * 在你潜艇上的探测仪发射器可以以 x (向前) 和 y (向上，如果是负数则向下) 速度发出探测仪，x 和 y 均为 [整型(integer)](https://en.wikipedia.org/wiki/Integer) 速度。
 * 比如，以 `x,y` 初始速度 `0,10` 为例，将会直接向上方发射探测仪，而初始速度像 `10,-1` 则会捎带着向下的倾角并向前发射探测仪。
 *
 * 以探测仪 `x,y` 开始的位置作为 `0,0`, 然后在 **步骤(steps)** 中沿着某种轨迹移动。在每一步中，速度和位置的改变按着如下的顺序发生：
 *
 * - 把探测仪的 `x` 位置加上它的 `x` 速度。
 * - 把探测仪的 `y` 位置加上它的 `y` 速度。
 * - 由于阻力，探测仪的 `x` 速度会朝着 `0` 值的方向变动 `1`；也就是，如果它大于 `0` 则减少 `1`, 如果它小于 `0` 则增加 `1`, 如果已经是 `0` 了则不做改变。
 * - 由于重力，探测仪的 `y` 速度减少 `1` 。
